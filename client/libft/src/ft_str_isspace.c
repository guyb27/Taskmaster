#include "../include/libft.h"

int		ft_str_isspace(char *str)
{
	int		i;

	i = 0;
	if (str && str[i])
	{
		while (str[i])
		{
			if (str[i] != ' ')
				return (0);
			i++;
		}
	}
	return (1);
}
