#include "libft.h"

void		ft_print_bits(char octet)
{
	int i;

	i = 256;
	while (i /= 2)
	{
		if (i <= octet)
		{
			ft_putchar('1');
			octet -= i;
		}
		else
			ft_putchar('0');
	}
}
