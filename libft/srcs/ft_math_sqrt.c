#include "libft.h"

int			ft_math_sqrt(int nb)
{
	int i;

	i = 1;
	if (nb <= 0 || nb >= FT_INT_MAX)
		return (0);
	while (i * i < nb)
		i++;
	return (!(nb % i) ? i : 0);
}
